# Constructor Expressions for Type Conversions
    # str -> class 'str'
    # type("110") -> class 'str'
    # int("110") -> 110
    # type(int("110")) -> class 'int' 
    # int -> class 'int'
    # type(110) -> class 'int'
    # str(110) -> '110'
    # type(str(110)) -> class 'str'